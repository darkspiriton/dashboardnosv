    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // Creamos las reglas de validaciÃ³n
        $rules = [
            'name'          => 'required',
        ];

        //Se va a pasar datos del producto, attributos y su cantidad
        try {
            // Ejecutamos el validador y en caso de que falle devolvemos la respuesta
            // con los errores
            $validator = \Validator::make($request->all(), $rules);
            if ($validator->fails()) {
                return response()->json(['message' => 'No posee todo los campos necesario para crear un tipo de producto'],401);
            }

            $type = Type::where('name','=',$request->input('name'))
                ->exists();
            if(!$type){
                $tipo= new Type();
                $tipo->name = $request->input('name');
                $tipo->save();
                return response()->json(['message' => 'El tipo de producto se agrego correctamente'],200);
            } else {
                return response()->json(['message' => 'El tipo de producto ya existe'],401);
            }

        } catch (Exception $e) {
            // Si algo sale mal devolvemos un error.
            return \Response::json(['message' => 'Ocurrio un error al agregar tipo de producto'], 500);
        }
    }

